    private void RenderBuildShopTier(string heading, int tier)
    {
        CreateText(controlRoot, heading, heading, 21, FontStyle.Bold, new Color(0.96f, 0.68f, 0.31f));
        foreach (var building in game.Buildings.Where(candidate => candidate.Tier == tier))
        {
            CreateShopBuildingEntry(building);
        }
    }

    private void CreateShopBuildingEntry(BuildingDefinition building)
    {
        var canBuild = CanBuildFromShop(building);
        var label = GetShopBuildingLabel(building, canBuild);
        var button = CreateButton(controlRoot, label, () =>
        {
            if (!CanBuildFromShop(building))
            {
                SetInfo($"{game.ActivePlayer.Name} cannot build {building.Name}: {GetBuildBlocker(building)}.");
                return;
            }

            game.Build(building.Name);
            Render();
        }, true, GetBuildingTooltip(building, canBuild));

        var image = button.GetComponent<Image>();
        image.color = canBuild ? new Color(1f, 0.72f, 0.30f) : new Color(0.34f, 0.32f, 0.30f);

        var labelText = button.GetComponentInChildren<Text>();
        if (labelText != null)
        {
            labelText.color = canBuild ? new Color(0.12f, 0.08f, 0.06f) : new Color(0.72f, 0.68f, 0.62f);
            labelText.alignment = TextAnchor.MiddleLeft;
        }

        var layout = button.GetComponent<LayoutElement>();
        layout.minHeight = 132;
    }

    private string GetShopBuildingLabel(BuildingDefinition building, bool canBuild)
    {
        var builtMarker = game.ActivePlayer.Buildings.Contains(building.Name) ? " [BUILT]" : string.Empty;
        return $"{building.Name}{builtMarker}\nTier {building.Tier}\nCost: {DescribeCost(building.Cost)}\nRequires: {GetShopRequirementText(building)}\nEffect: {GetBuildingEffectSummary(building)}\nStatus: {(canBuild ? "Can build" : GetBuildBlocker(building))}";
    }

    private bool CanBuildFromShop(BuildingDefinition building)
    {
        return game.CurrentPhase == Phase.Action && game.CanBuild(building);
    }

    private string GetShopRequirementText(BuildingDefinition building)
    {
        var requirements = new List<string>();
        if (!string.IsNullOrWhiteSpace(building.Requires))
        {
            requirements.Add(building.Requires);
        }

        if (building.Tier == 2)
        {
            requirements.Add("any other Tier 1 building");
        }

        if (building.MinClout > 0)
        {
            requirements.Add($"{building.MinClout} Clout");
        }

        return requirements.Count == 0 ? "None" : string.Join(" + ", requirements);
    }

    private string GetBuildingEffectSummary(BuildingDefinition building)
    {
        var cloutText = building.CloutReward > 0 ? $"Gain {building.CloutReward} Clout. " : string.Empty;
        return cloutText + GetBuildingBonusText(building.Name);
    }

