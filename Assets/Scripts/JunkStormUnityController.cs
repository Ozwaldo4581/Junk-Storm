    private void AssignExpeditionWorker(int outpostIndex)
    {
        if (game.CurrentPhase != Phase.Expedition)
        {
            SetInfo("Location tiles assign workers during the Expedition Phase.");
            return;
        }

        if (outpostIndex == DisplayJunkStormOutpost || outpostIndex == DisplayBiodomerOutpost)
        {
            var message = $"{game.ActivePlayer.Name} cannot visit {game.Outposts[outpostIndex].Name}; {GetUnsafeLocationSafetyReason(outpostIndex)}";
            SetInfo(message);
            return;
        }

        var limit = GetExpeditionWorkerLimit();
        if (limit <= 0)
        {
            SetInfo($"{game.ActivePlayer.Name} has no available workers to send. You may still click Skip Expedition.");
            return;
        }

        if (selectedExpeditionOutpost != outpostIndex)
        {
            selectedExpeditionOutpost = outpostIndex;
            assignedExpeditionWorkers = 1;
            SetInfo($"{game.ActivePlayer.Name} changed expedition destination to {game.Outposts[outpostIndex].Name}. Assigned workers reset to 1.");
            Render();
            return;
        }

        if (assignedExpeditionWorkers >= limit)
        {
            SetInfo($"{game.ActivePlayer.Name} cannot send more than {limit} workers.");
            return;
        }

        assignedExpeditionWorkers++;
        SetInfo($"{game.Outposts[outpostIndex].Name} selected. {assignedExpeditionWorkers} workers assigned.");
        Render();
    }

    private void ConfirmExpedition()
    {
        var limit = GetExpeditionWorkerLimit();
        if (selectedExpeditionOutpost < 0 || assignedExpeditionWorkers <= 0)
        {
            SetInfo("Choose a Location and assign at least 1 worker, or click Skip Expedition to send 0 workers.");
            return;
        }

        if (selectedExpeditionOutpost == DisplayJunkStormOutpost || selectedExpeditionOutpost == DisplayBiodomerOutpost)
        {
            SetInfo($"{game.ActivePlayer.Name} cannot visit {game.Outposts[selectedExpeditionOutpost].Name}; {GetUnsafeLocationSafetyReason(selectedExpeditionOutpost)}");
            return;
        }

        if (assignedExpeditionWorkers > limit)
        {
            SetInfo($"{game.ActivePlayer.Name} cannot send more than {limit} workers.");
            return;
        }

        var selectedOutpost = selectedExpeditionOutpost;
        var selectedWorkers = assignedExpeditionWorkers;
        ClearExpeditionSelectionState();
        game.Scavenge(selectedOutpost, selectedWorkers);
        Render();
    }

    private void SkipExpedition()
    {
        ClearExpeditionSelectionState();
        game.SkipExpedition();
        Render();
    }

    private void ClearExpeditionSelection()
    {
        ClearExpeditionSelectionState();
        SetInfo("Expedition selection cleared. Choose a Location and assign workers.");
        Render();
    }

    private void RemoveAssignedWorker()
    {
        if (assignedExpeditionWorkers <= 0)
        {
            ClearExpeditionSelection();
            return;
        }

        assignedExpeditionWorkers--;
        if (assignedExpeditionWorkers == 0)
        {
            selectedExpeditionOutpost = -1;
        }

        SetInfo("Removed 1 worker from the expedition selection.");
        Render();
    }

