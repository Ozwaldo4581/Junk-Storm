    {
        CreateText(boardRoot, "Phase", $"Generation {game.Generation} — {game.CurrentPhase} — Active: {game.ActivePlayer.Name}\nFirst Player: {game.Players[game.FirstPlayerIndex].Name}\nTurn Order: {game.GetTurnOrderText()}", 28, FontStyle.Bold, new Color(1f, 0.81f, 0.46f));


        foreach (var player in game.Players)
        {
            var buildings = player.Buildings.Count == 0 ? "No buildings" : string.Join(", ", player.Buildings);
            CreateText(
                boardRoot,
                player.Name,
                $"{player.Name} ({player.Character}){(player.Eliminated ? " — ELIMINATED" : string.Empty)}\nCurrent Location: {GetPlayerLocationText(player)}\nClout {player.Clout} | Worker Tokens {player.Workers} | Draw Pile {player.Deck.Count} | Recycle Pile {player.Discard.Count} | Discard Pile {player.Destroyed.Count}\nStored Resources — Alloy {player.Resources.Alloy}, Organics {player.Resources.Organics}, Plastoid {player.Resources.Plastoid}\nTemporary Labor — Labor {player.Resources.Labor}\nBuildings: {buildings}",
