                FontStyle.Normal,
                new Color(0.88f, 0.80f, 0.70f));
        }
    }

    private void RenderControls()
    {
        CreateText(controlRoot, "CenterTitle", "Cards / Hand / Actions", 24, FontStyle.Bold, new Color(1f, 0.81f, 0.46f));
        var activeHand = game.ActivePlayer.Hand.Count == 0 ? "Empty" : string.Join(", ", game.ActivePlayer.Hand);
        CreateText(controlRoot, "CurrentHand", $"{game.ActivePlayer.Name} Hand: {activeHand}", 18, FontStyle.Normal, new Color(0.90f, 0.82f, 0.72f));
        CreateText(controlRoot, "SelectedCardInfo", "Selected Card Info: hover a Recycle, Discard, attack, or build button to see details in the top info banner.", 16, FontStyle.Normal, new Color(0.80f, 0.72f, 0.64f));

        if (inputLocked)
        {
            CreateText(controlRoot, "Input Locked", "Threat movement in progress...", 24, FontStyle.Bold, new Color(1f, 0.81f, 0.46f));
            return;
        }

        if (game.CurrentPhase == Phase.Expedition)
        {
            CreateText(controlRoot, "ExpeditionHint", "Choose workers by clicking Location tiles on the right-side pentagon board.", 18, FontStyle.Bold, new Color(1f, 0.81f, 0.46f));
            CreateButton(controlRoot, "Confirm Expedition", ConfirmExpedition, true, "Confirm Expedition — Send assigned workers to the selected Location and resolve scavenging.");
            CreateButton(controlRoot, "Skip Expedition", SkipExpedition, true, "Skip Expedition — Send 0 worker tokens, choose no Location, and advance to the next player.");
            CreateButton(controlRoot, "Clear Expedition Selection", ClearExpeditionSelection, true, "Clear Expedition Selection — Remove the currently assigned workers and choose again.");
            CreateButton(controlRoot, "Remove 1 Worker", RemoveAssignedWorker, true, "Remove 1 Worker — Reduce the number of workers assigned to this expedition.");
        }
        else if (game.CurrentPhase == Phase.Action)
        {
            CreateButton(controlRoot, "Spend 1 Clout: Gain Worker Token", () =>
            {
                game.SpendCloutForWorkerToken();
                Render();
            }, game.ActivePlayer.Clout > 0, "Spend 1 Clout — Gain 1 worker token for future expeditions. Voluntary spending may reduce your Clout to 0 without eliminating you.");
            CreateButton(controlRoot, "Spend 1 Clout: Buy Worker Card", () =>
            {
                game.SpendCloutForWorkerCard();
                Render();
            }, game.ActivePlayer.Clout > 0, "Spend 1 Clout — Add 1 Worker card to your Recycle Pile. It remains in your deck cycle after reshuffling. Voluntary spending may reduce your Clout to 0 without eliminating you.");

            for (var index = 0; index < game.ActivePlayer.Hand.Count; index++)
            {
                var handIndex = index;
                var card = game.ActivePlayer.Hand[index];
                CreateButton(controlRoot, $"Recycle {card}", () =>
                {
                    game.PlayCard(handIndex, false);
                    Render();
                }, true, $"Recycle {card} — Use the card's recycle effect, then place it in your Recycle Pile. {GetCardTooltip(card)}");
                CreateButton(controlRoot, $"Discard {card}", () =>
                {
                    game.PlayCard(handIndex, true);
                    Render();
                }, true, $"Discard {card} — Use the card's stronger discard effect, then remove it from the game. {GetCardTooltip(card)}");
            }

            if (game.ActivePlayer.Hand.Contains("Soldier"))
            {
                var targets = game.GetSoldierAttackTargets(game.ActivePlayerIndex);
                if (targets.Count == 0)
                {
                    CreateButton(controlRoot, "Attack Player", () => { }, false, "No players at your Location to attack.");
                }
                else
                {
                    foreach (var targetIndex in targets)
                    {
                        var capturedTargetIndex = targetIndex;
                        CreateButton(controlRoot, $"Attack {game.Players[targetIndex].Name} with Soldier", () =>
                        {
                            game.SoldierAttack(game.ActivePlayerIndex, capturedTargetIndex);
                            Render();
                        }, true, $"Attack {game.Players[targetIndex].Name} — Discard Soldier to attack another player at your Location. If undefended, steal 1 random card from their hand if possible and make them lose 1 Clout.");
                    }
                }
            }

            CreateButton(controlRoot, "End Action", () =>
            {
                game.EndActionTurn();
                Render();
            }, true, "End Action — Finish this player's Action Phase turn and continue to the next player or the Junk Storm Phase.");
        }
        else if (game.CurrentPhase == Phase.Storm)
        {
            CreateButton(controlRoot, "Roll Junk Storm Die", () =>
            {
                StartCoroutine(ResolveStormStepwise());
            }, true, "Roll Junk Storm Die — Resolve the generation's hazard roll. 1–5 moves the Junk Storm; 6–10 moves the Biodomers, shown one Location at a time.");
        }
        else if (game.CurrentPhase == Phase.Reset)
        {
            CreateButton(controlRoot, "Reset Generation", () =>
            {
                game.ResetGeneration();
                Render();
            }, true, "Reset Generation — Recycle remaining hands, clear temporary flags, reset temporary Labor, draw back up, check victory, and start the next generation. Stored resources persist. Temporary Labor resets.");
        }
        else
        {
            CreateText(controlRoot, "Winner", $"{game.Winner} wins!", 30, FontStyle.Bold, new Color(0.62f, 0.90f, 0.58f));
        }

        RenderBuildShop();
    }

