    private string GetBuildBlocker(BuildingDefinition building)
    {
        if (game.CurrentPhase != Phase.Action)
        {
            return "building is only available during the Action Phase";
        }

        if (game.ActivePlayer.Buildings.Contains(building.Name))
        {
            return "already built";
        }

        if (building.MinClout > 0 && game.ActivePlayer.Clout < building.MinClout)
        {
            return $"requires {building.MinClout} Clout";
        }

        if (!string.IsNullOrWhiteSpace(building.Requires) && !game.ActivePlayer.Buildings.Contains(building.Requires))
        {
            return $"requires {building.Requires}";
        }

        if (building.Tier == 2 && game.ActivePlayer.Buildings.Count < 2)
        {
            return "requires two Tier 1 buildings";
        }

        return GetResourceShortfallText(building.Cost);
    }

    private string GetResourceShortfallText(ResourcePool cost)
    {
        var resources = game.ActivePlayer.Resources;
        var missing = new List<string>();
        if (resources.Organics < cost.Organics)
        {
            missing.Add($"needs {cost.Organics - resources.Organics} more Organics");
        }

        if (resources.Alloy < cost.Alloy)
        {
            missing.Add($"needs {cost.Alloy - resources.Alloy} more Alloy");
        }

        if (resources.Plastoid < cost.Plastoid)
        {
            missing.Add($"needs {cost.Plastoid - resources.Plastoid} more Plastoid");
        }

        if (resources.Labor < cost.Labor)
        {
            missing.Add($"needs {cost.Labor - resources.Labor} more Labor");
        }

        var remainingAlloy = Mathf.Max(0, resources.Alloy - cost.Alloy);
        var remainingPlastoid = Mathf.Max(0, resources.Plastoid - cost.Plastoid);
        var flexibleShortfall = cost.FlexibleAlloyPlastoid - remainingAlloy - remainingPlastoid;
        if (flexibleShortfall > 0)
        {
            missing.Add($"needs {flexibleShortfall} more Alloy/Plastoid");
        }

        return missing.Count == 0 ? "cannot afford the stored resource or temporary Labor cost" : string.Join(", ", missing);
    }

